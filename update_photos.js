const fs = require('fs');
const path = require('path');

const photosDir = path.join(__dirname, 'photos');
const outputFile = path.join(__dirname, 'photos.js');

try {
    const files = fs.readdirSync(photosDir).filter(file => {
        // Find all image files ignoring case and explicitly exclude the background pattern
        const isImage = file.match(/\.(jpg|jpeg|png|gif|heic|webp)$/i);
        const isNotBackground = file !== 'cute_fruits_animals_background.png' && file !== 'cute_invitation_background.png';
        return isImage && isNotBackground;
    });

    const fileContent = `// This file is auto-generated by update_photos.js\nwindow.galleryPhotos = ${JSON.stringify(files, null, 4)};\n`;
    fs.writeFileSync(outputFile, fileContent);
    console.log(`Successfully updated photos.js with ${files.length} images.`);
} catch (err) {
    if (err.code === 'ENOENT') {
        console.error('Error: "photos" directory not found.');
    } else {
        console.error('Error reading photos directory:', err);
    }
}
